CREATE DATABASE `HPSR`;

CREATE TABLE HPSR.`Immeuble` (
  `ImmeubleID` INT(10) NOT NULL,
  `ImmeubleNom` VARCHAR(30) NOT NULL,
  `Adresse` VARCHAR(50) NOT NULL UNIQUE,
  `Ville` VARCHAR(30) NOT NULL,
  `Province` VARCHAR(30) NOT NULL,
  `Code Postal` VARCHAR(10),
  PRIMARY KEY (`ImmeubleID`)
);

CREATE TABLE HPSR.`Preposer` (
  `PreposerID` INT(10) NOT NULL,
  `ImmeubleID` INT(10) NOT NULL,
  `DebutQuart` TIME NOT NULL,
  `FinQuart` TIME NOT NULL,
  `RepasHeureDebut` TIME NULL,
  `RepasHeureFin` TIME NULL,
  `PauseHeureDebut` TIME NULL,
  `PauseHeureFin` TIME NULL,
  PRIMARY KEY (`PreposerID`),
  FOREIGN KEY (`ImmeubleID`) REFERENCES `Immeuble`(`ImmeubleID`)
);

CREATE TABLE HPSR.`Porte` (
  `Porte` INT(10) NOT NULL,
  `LocataireNom` VARCHAR(30) NOT NULL,
  `LocatairePrenom` VARCHAR(30) NOT NULL,
  `ImmeubleID` INT(10) NOT NULL,
  PRIMARY KEY (`Porte`),
  FOREIGN KEY (`ImmeubleID`) REFERENCES `Immeuble`(`ImmeubleID`)
);

CREATE TABLE HPSR.`Locataire` (
  `LocataireID` INT(10) NOT NULL,
  `LocataireNom` VARCHAR(30) NOT NULL,
  `LocatairePrenom` VARCHAR(30) NOT NULL,
  `Telephone` VARCHAR(15) UNIQUE,
  `Porte` INT(10) NOT NULL,
  `Adresse` VARCHAR(150) NOT NULL UNIQUE,
  PRIMARY KEY (`LocataireID`),
  FOREIGN KEY (`Porte`) REFERENCES `Porte`(`Porte`)
);

CREATE TABLE HPSR.`Service` (
  `ServiceID` INT(10) NOT NULL,
  `ServiceNom` VARCHAR(30) NOT NULL UNIQUE,
  `NomJour` VARCHAR(20) NOT NULL,
  `LocataireID` INT(10) NOT NULL,
  `PreposerID` INT(10) NOT NULL,
  `HeureExecutionMin` TIME,
  `HeureExecutionMax` TIME,
  `HeureDebut` TIME,
  `HeureFin` TIME,
  `Duree` FLOAT,
  PRIMARY KEY (`ServiceID`),
  FOREIGN KEY (`PreposerID`) REFERENCES `Preposer`(`PreposerID`),
  FOREIGN KEY (`LocataireID`) REFERENCES `Locataire`(`LocataireID`)
);

CREATE TABLE HPSR.`Jour` (
  `NomJour` VARCHAR(20) NOT NULL,
  `ServiceID` INT(10) NOT NULL,
  `HeureTotal` FLOAT NOT NULL,
  PRIMARY KEY (`NomJour`),
  FOREIGN KEY (`ServiceID`) REFERENCES `Service`(`ServiceID`)
);

CREATE TABLE HPSR.`Schedule` (
  `NomJour` VARCHAR(30) NOT NULL,
  `Duree` INT(10) NOT NULL,
  `ServiceID` INT(10) NOT NULL,
  `LocataireID` INT(10) NOT NULL,
  FOREIGN KEY (`ServiceID`) REFERENCES `Service`(`ServiceID`),
  FOREIGN KEY (`LocataireID`) REFERENCES `Locataire`(`LocataireID`),
  FOREIGN KEY (`NomJour`) REFERENCES `Jour`(`NomJour`)
);

CREATE TABLE HPSR.`Directrice` (
  `UtilisateursID` INT(10) UNIQUE,
  `UserName` VARCHAR(30) UNIQUE,
  `UserPassword` VARCHAR(30),
  `Telephone` VARCHAR(10),
  PRIMARY KEY (`UtilisateursID`)
);


